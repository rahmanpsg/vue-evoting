class PemilihModel {
  constructor({ id, nik, nama, username, password, alamat, status }) {
    this.id = id;
    this.nik = nik;
    this.nama = nama;
    this.username = username;
    this.password = password;
    this.alamat = alamat;
    this.status = status;
  }
}

export default PemilihModel;
